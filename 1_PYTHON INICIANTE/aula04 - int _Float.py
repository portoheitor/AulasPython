'''
Tipos int e float
int -> Numero Inteiro
O tipo int representa qualquer valor numerico
Positivo ou negativo. int sem sinal é considerado Positivo
'''
print(11, type(11), sep= ' = ')
print(-11, type(-11), sep= ' = ')
print(0, type(0), sep= ' = ')


'''
float = numero componto flutuante
O tipo float reperesneta qualquer numero positivo ou negatvo
com ponto flutuante
float sem sinal é considerado positivo.
'''
print(0.0, type(0.0), sep= ' = ')
print(-2.5, type(2.5), sep= ' = ')
print(11.5, type(11.5), sep= ' = ')

'''
A class type mostra o tipo de dado que o Python
interpretou ao inferir o valor.
'''

